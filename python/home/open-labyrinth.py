def checkio(maze_map):
	move = {"S": (1, 0), "E": (0, 1), "N": (-1, 0), "W": (0, -1)}
	pos = (1,1)
	result = "S"
	while pos != (10,10):
		for d in move:
			next_pos = (move.get(d, None)[0] + pos[0], move.get(d, None)[1] + pos[1])
			if maze_map[next_pos[0]][next_pos[1]] == 2:
				continue
